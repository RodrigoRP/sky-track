export function searchAirports(query) {
  if (!query || query.length < 1) return []
  const q = query.toUpperCase()
  return airports
    .filter(
      (a) =>
        a.code.startsWith(q) ||
        a.city.toUpperCase().includes(q) ||
        a.country === q
    )
    .slice(0, 6)
}
